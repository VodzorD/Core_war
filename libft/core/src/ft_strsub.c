#include "../includes/coreft.h"

char		*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*sub;

	if (!len || !(sub = ft_strnew(len)))
		return (NULL);
	return (ft_strncpy(sub, (s + start), len));
}
