#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (!n || (*s1 == '\0' && *s2 == '\0'))
		return (0);
	if (*s1 == '\0' && *s2 != '\0')
		return (-1);
	if (*s1 != '\0' && *s2 == '\0')
		return (1);
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (ft_strncmp(++s1, ++s2, --n));
}
