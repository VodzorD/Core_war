#include "libft.h"

int		ft_int_len(int elem)
{
	int len;

	if (elem == -2147483648)
		return (11);
	len = (elem < 0) ? 1 : 0;
	elem = len ? -elem : elem;
	while (elem / 10 > 0)
	{
		elem /= 10;
		len++;
	}
	len++;
	return (len);
}
