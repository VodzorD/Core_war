#include "libft.h"

void	ft_putnbr(int n)
{
	char	str[11];
	int		i;

	i = 0;
	if (n < 0)
		ft_putchar('-');
	while (n >= 10 || n <= -10)
	{
		if (n > 0)
			str[i++] = n % 10 + '0';
		else
			str[i++] = n % 10 * -1 + '0';
		n = n / 10;
	}
	if (n < 0)
		n = n * -1;
	str[i] = n % 10 + '0';
	while (i >= 0)
		ft_putchar(str[i--]);
}
